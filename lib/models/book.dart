class Book {
  final String id;
  final String title;
  final String coverURL;
  final String author;
  final String host;
  final String hostPhone;
  final bool available;
  final String distance;

  Book({
    required this.id,
    required this.title,
    required this.coverURL,
    required this.author,
    required this.host,
    required this.hostPhone,
    required this.available,
    required this.distance,
  });
}
